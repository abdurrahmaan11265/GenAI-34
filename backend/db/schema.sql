-- -----------------------------------------------------
-- GRAPH_VERSIONS
-- -----------------------------------------------------
-- Named registry of every successfully completed graph
-- build per book. Single authoritative source for all
-- version lookups — replaces free integers across:
--   concepts.graph_version
--   concept_edges.graph_version
--   user_concept_state.graph_version
--
-- Design decisions encoded here:
--   1. books has NO current_graph_version column.
--      Current version = graph_versions WHERE is_current = TRUE.
--      One source of truth, enforced by trigger below.
--
--   2. user_books.pinned_graph_version_id is a UUID FK
--      to this table (ON DELETE RESTRICT), guaranteeing
--      every enrolled user references a real, validated
--      graph version. Naked integers are gone.
--
--   3. user_concept_state.graph_version is still a bare
--      INTEGER (joining through graph_versions to validate
--      it on every write would be expensive). It is an
--      application invariant: always set it from
--      graph_versions.version at write time.
--
-- Note: Only COMPLETED build jobs get a row here.
-- FAILED attempts stay in graph_build_jobs only.
-- -----------------------------------------------------

CREATE TABLE graph_versions (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    book_id UUID NOT NULL
        REFERENCES books(id)
        ON DELETE CASCADE,

    -- The integer version key (matches graph_build_jobs.graph_version,
    -- concepts.graph_version, concept_edges.graph_version)
    version INTEGER NOT NULL,

    -- FK back to the build job that produced this version
    build_job_id UUID NOT NULL
        REFERENCES graph_build_jobs(id)
        ON DELETE RESTRICT,

    -- Denormalised counts for fast info display (no join needed)
    node_count INTEGER NOT NULL DEFAULT 0,

    edge_count INTEGER NOT NULL DEFAULT 0,

    -- Human-readable label e.g. "v1 — initial build", "v2 — post-review"
    label TEXT,

    is_current BOOLEAN NOT NULL DEFAULT FALSE,

    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_graph_version_per_book UNIQUE (book_id, version),

    CONSTRAINT chk_gv_version    CHECK (version > 0),
    CONSTRAINT chk_gv_node_count CHECK (node_count >= 0),
    CONSTRAINT chk_gv_edge_count CHECK (edge_count >= 0)
);

CREATE INDEX idx_graph_versions_book    ON graph_versions(book_id);
CREATE INDEX idx_graph_versions_current ON graph_versions(book_id) WHERE is_current = TRUE;

-- Enforce exactly ONE is_current = TRUE per book
CREATE UNIQUE INDEX uq_current_graph_version_per_book
    ON graph_versions(book_id)
    WHERE is_current = TRUE;

-- Trigger: when a new graph_versions row is set is_current = TRUE,
-- automatically demote all other versions for that book.
CREATE OR REPLACE FUNCTION enforce_one_current_graph_version()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.is_current = TRUE THEN
        UPDATE graph_versions
        SET    is_current = FALSE
        WHERE  book_id = NEW.book_id
          AND  id      <> NEW.id
          AND  is_current = TRUE;
    END IF;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_one_current_graph_version
AFTER INSERT OR UPDATE OF is_current ON graph_versions
FOR EACH ROW
WHEN (NEW.is_current = TRUE)
EXECUTE FUNCTION enforce_one_current_graph_version();


