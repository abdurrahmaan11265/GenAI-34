-- because it's explicit, queryable with a partial index,
-- and survives version resets without ambiguity.
-- -----------------------------------------------------

CREATE TABLE learning_dna (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    user_id UUID NOT NULL
        REFERENCES users(id)
        ON DELETE CASCADE,

    dna_version INTEGER NOT NULL,

    dna_data JSONB NOT NULL,

    is_active BOOLEAN NOT NULL DEFAULT FALSE,

    generated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT chk_dna_version CHECK (dna_version > 0),

    CONSTRAINT uq_user_dna_version UNIQUE (user_id, dna_version)
);

CREATE INDEX idx_learning_dna_user   ON learning_dna(user_id);
CREATE INDEX idx_learning_dna_data   ON learning_dna USING GIN(dna_data);

-- Fast lookup: "what is this user's current DNA?"
CREATE UNIQUE INDEX uq_active_dna_per_user
    ON learning_dna(user_id)
    WHERE is_active = TRUE;

-- -----------------------------------------------------
-- ONE ACTIVE DNA TRIGGER
-- -----------------------------------------------------
-- When a new DNA row is set is_active = TRUE,
-- automatically deactivate all other versions for
-- that user. Prevents stale active flags.
-- -----------------------------------------------------

CREATE OR REPLACE FUNCTION enforce_one_active_dna()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.is_active = TRUE THEN
        UPDATE learning_dna
        SET    is_active = FALSE
        WHERE  user_id = NEW.user_id
          AND  id      <> NEW.id
          AND  is_active = TRUE;
    END IF;
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_one_active_dna
AFTER INSERT OR UPDATE OF is_active ON learning_dna
FOR EACH ROW
WHEN (NEW.is_active = TRUE)
EXECUTE FUNCTION enforce_one_active_dna();


