-- -----------------------------------------------------
-- CONCEPT_EDGES  (KG Edges — DAG)
-- -----------------------------------------------------
-- THE MOST CRITICAL TABLE.
-- Design: { from_node, to_node, type: prerequisite|related,
--            weight, confidence }
--
-- DAG INVARIANT:
--   Prerequisite edges MUST form a Directed Acyclic Graph.
--   Cycle detection runs in the ingestion pipeline (step 3)
--   BEFORE insertion using a topological sort. Any edge that
--   would create a cycle is rejected at the application layer.
--   The graph_build_job status moves to FAILED if a cycle is
--   detected after LLM extraction.
--
--   Viva answer: "We cannot enforce acyclicity purely in SQL
--   without a recursive CTE check on every INSERT (too
--   expensive). Instead, the pipeline validates the full edge
--   set with Kahn's algorithm before committing any edges.
--   The COMPLETED graph_build_job is proof of validation."
--
-- CROSS-BOOK INTEGRITY:
--   Nothing in the FK model prevents an edge where
--   from_concept.book_id != edge.book_id. This is enforced
--   by a BEFORE INSERT/UPDATE trigger below (trg_edge_same_book).
--
-- Edge semantics:
--   PREREQUISITE: from_node must be mastered before to_node
--                 is unlocked. to_node depends on from_node.
--   RELATED:      Informational link, no locking effect.
-- -----------------------------------------------------

CREATE TABLE concept_edges (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    book_id UUID NOT NULL
        REFERENCES books(id)
        ON DELETE CASCADE,

    graph_version INTEGER NOT NULL DEFAULT 1,

    from_concept_id UUID NOT NULL
        REFERENCES concepts(id)
        ON DELETE CASCADE,

    to_concept_id UUID NOT NULL
        REFERENCES concepts(id)
        ON DELETE CASCADE,

    edge_type edge_type NOT NULL DEFAULT 'PREREQUISITE',

    -- 0.0–1.0, LLM-generated confidence score for this edge
    confidence NUMERIC(5,4) NOT NULL DEFAULT 0.5,

    -- optional weight for graph traversal ordering
    weight NUMERIC(5,4) NOT NULL DEFAULT 1.0,

    -- edges flagged as human-verified skip re-validation
    is_verified BOOLEAN NOT NULL DEFAULT FALSE,

    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_concept_edge
        UNIQUE (book_id, graph_version, from_concept_id, to_concept_id, edge_type),

    CONSTRAINT chk_no_self_loop
        CHECK (from_concept_id <> to_concept_id),

    CONSTRAINT chk_confidence
        CHECK (confidence >= 0 AND confidence <= 1),

    CONSTRAINT chk_weight
        CHECK (weight >= 0),

    CONSTRAINT chk_graph_version_edge
        CHECK (graph_version > 0)
);

CREATE INDEX idx_concept_edges_book        ON concept_edges(book_id, graph_version);
CREATE INDEX idx_concept_edges_from        ON concept_edges(from_concept_id);
CREATE INDEX idx_concept_edges_to          ON concept_edges(to_concept_id);
CREATE INDEX idx_concept_edges_type        ON concept_edges(edge_type);
CREATE INDEX idx_concept_edges_unverified  ON concept_edges(book_id) WHERE is_verified = FALSE;

-- -----------------------------------------------------
-- CROSS-BOOK EDGE INTEGRITY TRIGGER
-- -----------------------------------------------------
-- Ensures both endpoint concepts belong to edge.book_id.
-- Prevents: concept from book A → concept from book B
-- which the FK model alone cannot prevent.
-- -----------------------------------------------------

CREATE OR REPLACE FUNCTION enforce_edge_same_book()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    from_book UUID;
    to_book   UUID;
BEGIN
    SELECT book_id INTO from_book FROM concepts WHERE id = NEW.from_concept_id;
    SELECT book_id INTO to_book   FROM concepts WHERE id = NEW.to_concept_id;

    IF from_book IS DISTINCT FROM NEW.book_id THEN
        RAISE EXCEPTION
            'concept_edges integrity: from_concept_id % belongs to book %, not edge.book_id %',
            NEW.from_concept_id, from_book, NEW.book_id;
    END IF;

    IF to_book IS DISTINCT FROM NEW.book_id THEN
        RAISE EXCEPTION
            'concept_edges integrity: to_concept_id % belongs to book %, not edge.book_id %',
            NEW.to_concept_id, to_book, NEW.book_id;
    END IF;

    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_edge_same_book
BEFORE INSERT OR UPDATE ON concept_edges
FOR EACH ROW EXECUTE FUNCTION enforce_edge_same_book();

