    ON mv_user_book_progress(user_id, book_id);

-- -----------------------------------------------------
-- mv_concept_performance
-- -----------------------------------------------------

CREATE MATERIALIZED VIEW mv_concept_performance
WITH (fillfactor = 90)
AS
SELECT
    ar.concept_id,
    COUNT(*)                                              AS attempts,
    ROUND(AVG(CASE WHEN ar.is_correct THEN 1 ELSE 0 END)::numeric, 4) AS accuracy_rate,
    ROUND(AVG(ar.confidence_level)::numeric, 2)          AS average_confidence,
    COUNT(*) FILTER (WHERE ar.is_correct = FALSE
                     AND ar.confidence_level >= 4)        AS confident_wrong_count
FROM assessment_responses ar
GROUP BY ar.concept_id
WITH NO DATA;

