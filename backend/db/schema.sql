-- -----------------------------------------------------
-- STREAK CALCULATION FUNCTION
-- -----------------------------------------------------
-- Correctly computes consecutive-day streak from
-- daily_activity, not just a row count.
-- Returns current streak length.
-- -----------------------------------------------------

CREATE OR REPLACE FUNCTION calculate_current_streak(p_user_id UUID)
RETURNS INTEGER
LANGUAGE plpgsql
AS $$
-- Algorithm:
--   Walk activity_date DESC from the most recent day.
--   A streak is consecutive if each row is exactly 1 day
--   before the previous one we accepted.
--
--   Edge cases handled:
--   1. No activity today — start from yesterday (streak still
--      valid if user studied yesterday and before).
--   2. Gap in the middle — stop immediately on any break.
--   3. Duplicate dates prevented by UNIQUE constraint.
--
-- Implementation note: calculate_current_streak() is a DB
-- convenience. The canonical streak value is stored in
-- learning_streaks.current_streak_days and is the source
-- of truth for the dashboard. This function is used only
-- to recompute it when a daily_activity row is upserted.
DECLARE
    streak      INTEGER := 0;
    check_date  DATE    := CURRENT_DATE;
    prev_date   DATE    := NULL;
    rec         RECORD;
BEGIN
    SELECT MAX(activity_date)
    INTO   prev_date
    FROM   daily_activity
    WHERE  user_id = p_user_id;

    -- No activity at all
    IF prev_date IS NULL THEN
        RETURN 0;
    END IF;

    -- Most recent activity is older than yesterday: streak already broken
    IF prev_date < CURRENT_DATE - INTERVAL '1 day' THEN
        RETURN 0;
    END IF;

    -- Walk backwards counting consecutive days
    FOR rec IN (
        SELECT activity_date
        FROM   daily_activity
        WHERE  user_id = p_user_id
          AND  activity_date <= CURRENT_DATE
        ORDER  BY activity_date DESC
    )
    LOOP
        IF streak = 0 THEN
            -- First row: must be today or yesterday to start streak
            IF rec.activity_date >= CURRENT_DATE - INTERVAL '1 day' THEN
                streak     := 1;
                check_date := rec.activity_date;
            ELSE
                EXIT;  -- first ever activity is older than yesterday
            END IF;
        ELSIF rec.activity_date = check_date - INTERVAL '1 day' THEN
            streak     := streak + 1;
            check_date := rec.activity_date;
        ELSE
            EXIT;  -- gap found — stop
        END IF;
    END LOOP;

    RETURN streak;
END;
$$;

-- -----------------------------------------------------
-- AUTO-UPDATE search_vector TRIGGER ON concepts
-- -----------------------------------------------------
-- Keeps tsvector in sync whenever name or summary changes.
-- Uses 'english' dictionary; adjust for other locales.
-- -----------------------------------------------------

CREATE OR REPLACE FUNCTION concepts_search_vector_update()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.search_vector :=
        setweight(to_tsvector('english', COALESCE(NEW.name, '')),    'A') ||
        setweight(to_tsvector('english', COALESCE(NEW.summary, '')), 'B') ||
        setweight(to_tsvector('english', COALESCE(NEW.learning_objective, '')), 'C');
    RETURN NEW;
END;
$$;

CREATE TRIGGER trg_concepts_search_vector
BEFORE INSERT OR UPDATE OF name, summary, learning_objective
ON concepts
FOR EACH ROW EXECUTE FUNCTION concepts_search_vector_update();

