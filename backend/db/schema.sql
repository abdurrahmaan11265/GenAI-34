-- -----------------------------------------------------
-- LEARNING DNA
-- -----------------------------------------------------
-- Personalized learner model, versioned. Append-only.
--
-- is_active: exactly ONE row per user should be active
-- at any time. Enforced by trg_one_active_dna trigger.
-- Activating a new version deactivates all prior ones.
