-- mv_user_book_progress
-- -----------------------------------------------------

CREATE MATERIALIZED VIEW mv_user_book_progress
WITH (fillfactor = 90)
AS
SELECT
    cm.user_id,
    c.book_id,
    COUNT(*)                                              AS total_concepts,
    COUNT(*) FILTER (WHERE cm.mastery_state = 'MASTERED') AS mastered_concepts,
    COUNT(*) FILTER (WHERE cm.mastery_state = 'LEARNING') AS learning_concepts,
    COUNT(*) FILTER (WHERE cm.mastery_state = 'FORGOTTEN') AS forgotten_concepts,
    ROUND(AVG(cm.mastery_score)::numeric, 4)              AS average_mastery
FROM concept_mastery cm
JOIN concepts c ON c.id = cm.concept_id
GROUP BY cm.user_id, c.book_id
WITH NO DATA;

