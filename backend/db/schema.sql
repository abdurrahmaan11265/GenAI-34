-- -----------------------------------------------------
-- USER_BOOKS
-- -----------------------------------------------------
-- Junction: a user enrolls in a book.
-- pinned_graph_version_id: FK to graph_versions.
--   Locks the user to the exact graph version that existed
--   when they enrolled, so upstream graph rebuilds don't
--   silently break their in-progress learning path.
